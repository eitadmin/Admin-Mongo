package com.eiw.client.gxtmodel;

import com.extjs.gxt.ui.client.data.BaseModel;

public class ModelUserPreferenceData extends BaseModel {

	public ModelUserPreferenceData() {
	}

	public ModelUserPreferenceData(int id, String name, String title,
			String description, String shape, String latlng, String status,
			String plateNo) {
		set("id", id);
		set("name", name);
		set("title", title);
		set("description", description);
		set("shape", shape);
		set("latlng", latlng);
		set("status", status);
		set("plateNo", plateNo);

	}

	public int getId() {
		return get("id");
	}

	public String getName() {
		return (String) get("name");
	}

	public String getTitle() {
		return (String) get("title");
	}

	public String getDescription() {
		return (String) get("description");
	}

	public String getShape() {
		return (String) get("shape");
	}

	public String getLatlng() {
		return (String) get("latlng");
	}

	public String getStatus() {
		return (String) get("status");
	}

	public String getPlateNo() {
		return (String) get("plateNo");
	}
}
