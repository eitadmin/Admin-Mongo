package com.skt.client.dto;

import java.util.Map;

import com.eiw.server.studenttrackingpu.StudentGeozone;

public class SktInitData {
	private Map<String, RouteAndVehicleData> studentVehicleDataMap;
	private Map<String, String> alerttypesDescriptionMap;
	private Map<String, StudentGeozone> studentGeoZoneMap;

	public Map<String, RouteAndVehicleData> getSchoolRoutes() {
		return studentVehicleDataMap;
	}

	public void setSchoolRoutes(
			Map<String, RouteAndVehicleData> studentVehicleDataMap) {
		this.studentVehicleDataMap = studentVehicleDataMap;
	}

	public Map<String, String> getAlerttypesDescriptionMap() {
		return alerttypesDescriptionMap;
	}

	public void setAlerttypesDescriptionMap(
			Map<String, String> alerttypesDescriptionMap) {
		this.alerttypesDescriptionMap = alerttypesDescriptionMap;
	}
	
	public Map<String, StudentGeozone> getStudentGeoZoneMap() {
		return studentGeoZoneMap;
	}

	public void setStudentGeoZoneMap(Map<String, StudentGeozone> studentGeoZoneMap) {
		this.studentGeoZoneMap = studentGeoZoneMap;
	}
}
