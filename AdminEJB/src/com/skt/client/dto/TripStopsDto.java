package com.skt.client.dto;

import java.io.Serializable;
import java.util.Date;

public class TripStopsDto implements Serializable {
	private int orderno;
	private long stoppointId;
	private String latlan;
	private String stopName;
	private int tripId;
	private Date arrivaltime;
	private String triptype;

	public String getTriptype() {
		return triptype;
	}

	public void setTriptype(String triptype) {
		this.triptype = triptype;
	}

	public int getOrderno() {
		return orderno;
	}

	public void setOrderno(int orderno) {
		this.orderno = orderno;
	}

	public long getStoppointId() {
		return stoppointId;
	}

	public void setStoppointId(long stoppointId) {
		this.stoppointId = stoppointId;
	}

	public String getLatlan() {
		return latlan;
	}

	public void setLatlan(String latlan) {
		this.latlan = latlan;
	}

	public String getStopName() {
		return stopName;
	}

	public void setStopName(String stopName) {
		this.stopName = stopName;
	}

	public int getTripId() {
		return tripId;
	}

	public void setTripId(int tripId) {
		this.tripId = tripId;
	}

	public Date getArrivaltime() {
		return arrivaltime;
	}

	public void setArrivaltime(Date arrivaltime) {
		this.arrivaltime = arrivaltime;
	}
}
